// Variables in javascript are declared using the var keyword, let keyword and const keyword.
// The syntax for declaring a variable is:
// var variableName;
// Here, variableName is the name of the variable.
// A variable name can contain letters, digits, underscores, and dollar signs.
// A variable name cannot start with a digit.
// A variable name is case-sensitive.
// A variable name cannot be a reserved keyword.
// A variable name should be meaningful and descriptive.
// A variable name should be camelCase.
// A variable name should not contain spaces.
// A variable name should not contain special characters.

// Example for declaring variables
var x;
var y;
var firstName;
var lastName;
var age;
var isMarried;

// Variables can also be declared and initialized in a single line.

// Example for declaring and initializing variables
var x = 10;

// In the above example, we have declared a variable x and initialized it with the value 10.
// We can also declare multiple variables in a single line.

// Example for declaring multiple variables

var a, b, c;

// In the above example, we have declared three variables a, b, and c in a single line.

// Variables can also be declared and initialized with values of different data types.

// Example for declaring and initializing variables with different data types
var name = "John";
var age = 25;
var isMarried = false;

// In the above example, we have declared three variables name, age, and isMarried and initialized them with values of different data types.

// Now let's understand let and const

// The let keyword was introduced in ES6 (ECMAScript 2015) to declare block-scoped variables.
// The syntax for declaring a variable using the let keyword is:
// let variableName;

// Example for declaring variables using let
let x;
let y;
let firstName;

// The const keyword was also introduced in ES6 (ECMAScript 2015) to declare block-scoped variables.
// The syntax for declaring a variable using the const keyword is:
// const variableName;

// Example for declaring variables using const
const PI = 3.14;

// In the above example, we have declared a constant PI and initialized it with the value 3.14.

// A constant cannot be reassigned a new value once it is initialized.
// A constant must be initialized with a value.

// Variables declared using the var keyword are function-scoped.
// Variables declared using the let and const keywords are block-scoped.
